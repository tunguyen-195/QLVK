package com.qlvk.model;

public class LanhDaoModel {

	private String soHieuCAND;
	private String hoTen;
	private String sdt;
	private String nhanHieu;
	private int soLuong;
	private String lyDo;
	private int maMuon;

	public String getSoHieuCAND() {
		return soHieuCAND;
	}

	public void setSoHieuCAND(String soHieuCAND) {
		this.soHieuCAND = soHieuCAND;
	}

	public String getHoTen() {
		return hoTen;
	}

	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}

	public String getSdt() {
		return sdt;
	}

	public void setSdt(String sdt) {
		this.sdt = sdt;
	}

	public String getNhanHieu() {
		return nhanHieu;
	}

	public void setNhanHieu(String nhanHieu) {
		this.nhanHieu = nhanHieu;
	}

	public int getSoLuong() {
		return soLuong;
	}

	public void setSoLuong(int soLuong) {
		this.soLuong = soLuong;
	}

	public String getLyDo() {
		return lyDo;
	}

	public void setLyDo(String lyDo) {
		this.lyDo = lyDo;
	}

	public int getMaMuon() {
		return maMuon;
	}

	public void setMaMuon(int maMuon) {
		this.maMuon = maMuon;
	}

}
