package com.qlvk.model;

public class ChoMuonModel {
	private int maMuon;
	private int maDuyet;
	private int soHieuVK;
	private int soLuong;
	private String nhanHieuVK;

	public int getMaMuon() {
		return maMuon;
	}

	public void setMaMuon(int maMuon) {
		this.maMuon = maMuon;
	}

	public int getMaDuyet() {
		return maDuyet;
	}

	public void setMaDuyet(int maDuyet) {
		this.maDuyet = maDuyet;
	}

	public int getSoHieuVK() {
		return soHieuVK;
	}

	public void setSoHieuVK(int soHieuVK) {
		this.soHieuVK = soHieuVK;
	}

	public int getSoLuong() {
		return soLuong;
	}

	public void setSoLuong(int soLuong) {
		this.soLuong = soLuong;
	}

	public String getNhanHieuVK() {
		return nhanHieuVK;
	}

	public void setNhanHieuVK(String nhanHieuVK) {
		this.nhanHieuVK = nhanHieuVK;
	}

}
